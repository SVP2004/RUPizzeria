package com.example.rupizzeriaapp;

/**
 * Enum representing the available sizes for a pizza.
 * This enum is used to specify and manage the size of a pizza in the application.
 * @author Sahil Patel, Shreyas Santosh
 */

public enum Size {

    /**
     * Represents a small-sized pizza.
     */
    SMALL,

    /**
     * Represents a medium-sized pizza.
     */
    MEDIUM,

    /**
     * Represents a large-sized pizza.
     */
    LARGE
}
